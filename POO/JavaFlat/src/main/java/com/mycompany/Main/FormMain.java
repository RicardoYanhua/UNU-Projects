    public void InitTabla() {
        TabPanel.putClientProperty(FlatClientProperties.STYLE,
                "tabType: card ; font: +3; tabHeight: 50;tabInsets: 0,100,0,100");

        TablaAutores.getTableHeader().setReorderingAllowed(false);

        TablaAutores.getTableHeader().getColumnModel().getColumn(0).setMaxWidth(100);

        TableCellRenderer headerRenderer = TablaAutores.getTableHeader().getDefaultRenderer();
        if (headerRenderer instanceof JLabel) {
            JLabel headerLabel = (JLabel) headerRenderer;
            headerLabel.setHorizontalAlignment(SwingConstants.CENTER);
        }
        TableCellRenderer center = new DefaultTableCellRenderer() {
            {
                setHorizontalAlignment(SwingConstants.CENTER);
            }
        };
        TablaAutores.getColumnModel().getColumn(0).setCellRenderer(center);

        TablaAutores.getTableHeader().putClientProperty(FlatClientProperties.STYLE, ""
                + "height:50;"
                + "font: +3 ;"
                + "hoverBackground:null;"
                + "pressedBackground:null;"
                + "separatorColor:$TableHeader.background;");
        TablaAutores.putClientProperty(FlatClientProperties.STYLE, ""
                + "rowHeight:40;"
                + "intercellSpacing:0,1;"
                + "cellFocusColor:$TableHeader.hoverBackground;"
                + "selectionBackground:$TableHeader.hoverBackground;"
                + "selectionInactiveBackground:$TableHeader.hoverBackground;"
                + "selectionForeground:$Table.foreground;");
        jScrollPane1.getVerticalScrollBar().putClientProperty(FlatClientProperties.STYLE, ""
                + "trackArc:$ScrollBar.thumbArc;"
                + "trackInsets:3,3,3,3;"
                + "thumbInsets:3,3,3,3;"
                + "background:$Table.background;");
        TablaAutores.setShowHorizontalLines(true);
        TablaAutores.setShowVerticalLines(true);
        TablaAutores.setSelectionMode(ListSelectionModel.SINGLE_SELECTION);
        DataAutor = new AutorControl((DefaultTableModel) TablaAutores.getModel());

    }

    public void MostrarTablaAutores() {
        DataAutor.MostrarAutores();
    }

