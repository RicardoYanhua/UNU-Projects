    private Option getSelectedOption() {
        Option option = GlassPanePopup.createOption();
        option.setOpacity(0.0f);
        Location h = Location.CENTER;
        Location v = Location.BOTTOM;
        Option.BackgroundClickType backgroundClickType = Option.BackgroundClickType.CLOSE_MODAL;
        option.setAnimationEnabled(true)
                .setCloseOnPressedEscape(true)
                .setBackgroundClickType(backgroundClickType);
        option.getLayoutOption().setLocation(h, v);
        return option;
    }

