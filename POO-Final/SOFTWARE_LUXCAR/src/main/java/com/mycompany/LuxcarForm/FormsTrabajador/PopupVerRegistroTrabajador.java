    public PopupVerRegistroTrabajador(TrabajadorClass Trabajador) {
        this();

        NOMBRES_APELLIDOS.setText(Trabajador.getNombresApellidos());
        DIRECCION.setText(Trabajador.getDireccion());
        DNI.setText(Trabajador.getDNI());
        CORREO.setText(Trabajador.getCorreo());
        PUESTO.setText(Trabajador.getPuesto());
        TELEFONO.setText(Utilidades.setNumerox9_51(Trabajador.getTelefono()));
        GENERO.setText(Trabajador.getGenero());
        
        if(Trabajador.getFoto() == null){
            CargarImagenDefault();
        }else{
            try {
                IMAGEN.setBackground_Image( Utilidades.bytesToImageIcon(Trabajador.getFoto()));
            } catch (IOException ex) {
                Logger.getLogger(PopupModificarTrabajador.class.getName()).log(Level.SEVERE, null, ex);
            }
            
        }

        ControlServicio cs = new ControlServicio();
        DefaultTableModel service_model = cs.getAllServiceTrabajador(Trabajador.getDNI(), (DefaultTableModel) TABLA_SERVICIO.getModel());
        TABLA_SERVICIO.setModel(service_model);

    }

