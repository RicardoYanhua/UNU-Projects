    public boolean ValidarDatos() {

        if (getContraseña().equals("")) {
            new PopupMessage("CONTRASEÑA", "El campo contraseña esta vacio.", 2);
            return false;
        } else if (getConfirContraseña().equals("")) {
            new PopupMessage("CONFIRMAR CONTRASEÑA", "El campo confirmar contraseña esta vacio.", 2);
            return false;
        } else if (getContraseña().length() < 5) {
            new PopupMessage("CONTRASEÑA", "La contraseña es muy corta.", 2);
            return false;
        } else if (!getContraseña().equals(getConfirContraseña())) {
            new PopupMessage("CONTRASEÑA", "Las contraseñas no coinciden", 2);
            return false;
        } else {
            return true;
        }
    }

    public String getContraseña() {
        return CONTRASENIA.getText();
    }

    public String getConfirContraseña() {
        return CONFIR_CONTRASEÑA.getText();
    }

