    private void BTN_GUARDARActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BTN_GUARDARActionPerformed
        // TODO add your handling code here:
        if (ValidarDatos()) {

            ControlUsuario cu = new ControlUsuario();
            UsuarioClass u = new UsuarioClass(DNI_TRABAJADOR.getText(), getContraseña(), false);
            cu.InsertarUsuario(u);
            cu.Disconneting();

            FormUsuario.getCountUsers();
            FormUsuario.DataUsuario.MostrarBusquedaDeTrabajadorSinUsuario(FormUsuario.TXT_BUSQUEDA.getText());
            
            GlassPanePopup.closePopupLast();
            new PopupMessage(
                    "¡ CREACION DE USUARIO EXITOSO !",
                    "Se ha cambiado correctamente la contraseña del usuario",
                    0
            );

        }

    }//GEN-LAST:event_BTN_GUARDARActionPerformed

